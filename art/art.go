// DrawArt generates pretty art on stderr
func DrawArt() {
	quotes := [...]string{
		"https request fuzzer/brute forcer",
		"fuzz everything",
		"why pay that much for a brute force tool???",
		"uwu stay hydrated hacker",
		"LOOK AT YOU, HACKER",
		"fuzzing/bruting enterprise solutions",
		"INSTALL GENTOO",
		"no more secrets",
		"everything is about sex, except sex, which is about power",
		"smash the state",
		"the web was never designed with security in mind",
	}

	art := [...]string{
		art1,
		art2,
		art3,
		art4,
		art5,
		art6,
		art7,
		art8,
		art9,
		art10,
	}

	s1 := rand.NewSource(time.Now().UnixNano())
	r1 := rand.New(s1)

	randQuote := quotes[r1.Intn(11)]
	randArt := art[r1.Intn(10)]

	var artColor = ct.Fg(ct.Magenta).Paint
	var quoteColor = ct.Fg(ct.Cyan).Paint

	fmt.Fprintln(os.Stderr, artColor(randArt))
	fmt.Fprintln(os.Stderr, quoteColor(randQuote))
}
